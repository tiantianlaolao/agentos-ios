import Foundation

/// Lightweight localization system with embedded zh/en dictionaries.
/// Uses the saved "locale" setting from DatabaseService, falling back to the
/// system's preferred language.
@MainActor
final class L10n {
    static let shared = L10n()

    private(set) var locale: String = "en"
    private var translations: [String: String] = [:]

    // MARK: - Init

    private init() {
        // Detect system language on first launch
        let systemLang = Locale.preferredLanguages.first ?? "en"
        locale = systemLang.hasPrefix("zh") ? "zh" : "en"
        translations = locale == "zh" ? Self.zh : Self.en
    }

    // MARK: - Public API

    /// Translate a dot-separated key, e.g. "chat.hubTitle"
    static func tr(_ key: String) -> String {
        shared.translations[key] ?? key
    }

    /// Translate with parameter interpolation: {{name}} -> value
    static func tr(_ key: String, _ params: [String: String]) -> String {
        var result = tr(key)
        for (k, v) in params {
            result = result.replacingOccurrences(of: "{{\(k)}}", with: v)
        }
        return result
    }

    /// Reload locale from DatabaseService (call on app launch / settings change)
    func loadLocale() async {
        if let saved = try? await DatabaseService.shared.getSetting(key: "locale"),
           !saved.isEmpty {
            locale = saved
        } else {
            let systemLang = Locale.preferredLanguages.first ?? "en"
            locale = systemLang.hasPrefix("zh") ? "zh" : "en"
        }
        translations = locale == "zh" ? Self.zh : Self.en
    }

    /// Force-set locale (called from SettingsView when user changes language)
    func setLocale(_ loc: String) {
        locale = loc
        translations = loc == "zh" ? Self.zh : Self.en
    }

